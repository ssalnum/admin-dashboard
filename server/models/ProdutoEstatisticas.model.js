import mongoose from "mongoose";

const ProdutoEstatisticasSchema = new mongoose.Schema(
  {
    productId: String,
    yearlySalesTotal: Number,
    yearlyTotalSoldUnits: Number,
    year: Number,
    monthlyData: [
      {
        month: String,
        totalSales: Number,
        totalUnits: Number,
      },
    ],
    dailyData: [
      {
        date: String,
        totalSales: Number,
        totalUnits: Number,
      },
    ],
  },
  { timestamps: true }
);

const ProdutoEstatisticas = mongoose.model("ProdutoEstatisticas", ProdutoEstatisticasSchema);
export default ProdutoEstatisticas;